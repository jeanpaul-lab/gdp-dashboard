        "        st.metric(\"Max Savings\", f\"RWF {savings:,.0f}\", \"Reuse & Sell Surplus\")\n",
        "\n",
        "        st.subheader(\"Advanced Metrics\")\n",
        "        col4, col5 = st.columns(2)\n",
        "        col4.metric(\"Max Cut\", f\"{max_cut:,.0f} m³ at {max_cut_station} m\")\n",
        "        col4.metric(\"Max Fill\", f\"{max_fill:,.0f} m³ at {max_fill_station} m\")\n",
        "        col4.metric(\"Free Haul Volume\", f\"{free_haul_volume:,.0f} m³\")\n",
        "        col5.metric(\"Avg Haul Distance\", f\"{avg_haul_distance:,.1f} m\")\n",
        "        col5.metric(\"Avg Haul Volume\", f\"{avg_haul_volume:,.0f} m³\")\n",
        "        st.write(f\"Balance Points: {', '.join(map(str, balance_points))} m\")\n",
        "\n",
