        "        col1.metric(\"Adjusted Cut\", f\"{total_adj_cut:,.1f} m³\")\n",
        "        col2.metric(\"Total Fill\", f\"{total_fill:,.0f} m³\")\n",
        "        col2.metric(\"Borrow Needed\", f\"{borrow:,.1f} m³\")\n",
        "        col3.metric(\"Surplus Cut\", f\"{surplus:,.1f} m³\")\n",
        "        col3.metric(\"Net Revenue\", f\"RWF {revenue - borrow*8000:,.0f}\")\n",
