        "uploaded_file = st.file_uploader(\"Upload CSV: Chainage (m), Cut Volume (m³), Fill Volume (m³), Shrinkage Factor\", type=\"csv\")\n",
        "\n",
        "if uploaded_file is not None:\n",
        "    df = pd.read_csv(uploaded_file)\n",
        "    if df.shape[1] != 4:\n",
        "        st.error(\"CSV must have 4 columns: Chainage (m), Cut Volume (m³), Fill Volume (m³), Shrinkage Factor\")\n",
        "    else:\n",
        "        st.success(\"Data loaded!\")\n",
        "        st.dataframe(df.style.format(\"{:.1f}\"))\n",
        "\n",
        "        # === CALCULATIONS ===\n",
        "        df['Adjusted Cut'] = df['Cut Volume (m³)'] / df['Shrinkage Factor']\n",
        "        df['Net Volume'] = df['Adjusted Cut'] - df['Fill Volume (m³)']\n",
        "        df['Cumulative'] = df['Net Volume'].cumsum()\n",
        "\n",
        "        # Max Cut/Fill\n",
        "        max_cut = df['Cut Volume (m³)'].max()\n",
        "        max_fill = df['Fill Volume (m³)'].max()\n",
        "        max_cut_station = df[df['Cut Volume (m³)'] == max_cut]['Chainage (m)'].iloc[0]\n",
        "        max_fill_station = df[df['Fill Volume (m³)'] == max_fill]['Chainage (m)'].iloc[0]\n",
        "\n",
        "        # Balance Points\n",
        "        balance_points = []\n",
        "        for i in range(len(df)-1):\n",
        "            y1, y2 = df['Cumulative'].iloc[i], df['Cumulative'].iloc[i+1]\n",
        "            if y1 * y2 <= 0 and y1 != y2:\n",
        "                x1, x2 = df['Chainage (m)'].iloc[i], df['Chainage (m)'].iloc[i+1]\n",
        "                zero_at = x1 + (x2 - x1) * abs(y1) / (abs(y1) + abs(y2))\n",
        "                balance_points.append(round(zero_at))\n",
        "\n",
        "        # Free Haul (100m)\n",
        "        free_haul_distance = 100\n",
        "        free_haul_volume = 0\n",
        "        for i in range(len(df)-1):\n",
        "            dist = df['Chainage (m)'].iloc[i+1] - df['Chainage (m)'].iloc[i]\n",
        "            vol = abs(df['Net Volume'].iloc[i])\n",
        "            if dist <= free_haul_distance:\n",
        "                free_haul_volume += vol\n",
        "\n",
        "        # Average Haul\n",
        "        total_haul_volume = 0\n",
        "        total_haul_distance = 0\n",
        "        for i in range(len(df)-1):\n",
        "            dist = df['Chainage (m)'].iloc[i+1] - df['Chainage (m)'].iloc[i]\n",
        "            vol = abs(df['Net Volume'].iloc[i])\n",
        "            if dist > free_haul_distance:\n",
        "                total_haul_volume += vol\n",
        "                total_haul_distance += vol * (dist - free_haul_distance)\n",
        "\n",
        "        avg_haul_distance = total_haul_distance / total_haul_volume if total_haul_volume > 0 else 0\n",
        "        avg_haul_volume = total_haul_volume\n",
        "\n",
        "        # Summary\n",
        "        total_raw_cut = df['Cut Volume (m³)'].sum()\n",
        "        total_adj_cut = df['Adjusted Cut'].sum()\n",
        "        total_fill = df['Fill Volume (m³)'].sum()\n",
        "        borrow = max(0, total_fill - total_adj_cut)\n",
        "        surplus = max(0, total_adj_cut - total_fill)\n",
        "        savings = borrow * 8000\n",
        "        revenue = surplus * 8000\n",
        "\n",
        "        # === DISPLAY REPORT ===\n",
        "        st.subheader(\"Earthwork Summary\")\n",
        "        col1, col2, col3 = st.columns(3)\n",
